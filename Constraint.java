public class Constraint
{
    public int minAnAbs, maxAnAbs;
    public int minAnExp, maxAnExp;
    public double minMedAcad, maxMedAcad;

    public void setMinAnAbs(int minAnAbs)
    {
        this.minAnAbs = minAnAbs;
    }

    public void setMaxAnAbs(int maxAnAbs)
    {
        this.maxAnAbs = maxAnAbs;
    }

    public void setMinAnExp(int minAnExp)
    {
        this.minAnExp = minAnExp;
    }

    public void setMaxAnExp(int maxAnExp)
    {
        this.maxAnExp = maxAnExp;
    }

    public void setMinMedAcad(double minMedAcad)
    {
        this.minMedAcad = minMedAcad;
    }

    public void setMaxMedAcad(double maxMedAcad)
    {
        this.maxMedAcad = maxMedAcad;
    }
}
